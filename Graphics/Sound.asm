;-------------------- Definitions --------------------
BELL_ASCII      EQU 07H



;-----------------------------------------------------
; Beep
;-----------------------------------------------------
; Plays Bell sound
;-----------------------------------------------------
Beep MACRO

MOV AH, 02
MOV DL, BELL_ASCII
INT 21H

ENDM Beep